--> CREATE NEW TABLES <--
-->Table names are defined within "" to ensure PostgreSQL does not make them lowercase

-->Create the STUDENTS table which will be used for logging into the application
CREATE TABLE "STUDENTS" (
 STUDENTNO					NUMERIC(9) NOT NULL,
 PASSWORD					CHAR(32) NOT NULL,
 CONSTRAINT "STUDENTS_PRIMARY_KEY" PRIMARY KEY (STUDENTNO));
 
-->Create the SESSION TYPES table which contains all possible types of sessions that can be schedules (i.e tutorials)
CREATE TABLE "SESSIONTYPES" (
 SESSIONTYPE				CHAR(12) NOT NULL,
 CONSTRAINT "SESSIONTYPES_PRIMARY_KEY" PRIMARY KEY (SESSIONTYPE));
 
-->Create the TUTORS table which contains the staff memebers who can tech on modules
CREATE TABLE "TUTORS" (
 TUTORID					NUMERIC(9) NOT NULL,
 NAME						CHAR(32),
 EMAIL						CHAR(48),
 PHONENO					CHAR(4),
 CONSTRAINT "TUTORS_PRIMARY_KEY" PRIMARY KEY (TUTORID));
 
-->Create the SEMESTERS table which contains the semesters in a year
CREATE TABLE "SEMESTERS" (
 SEMESTERCODE				CHAR(1) NOT NULL,
 DESCRIPTION				CHAR(16),
 CONSTRAINT "SEMESTERS_PRIMARY_KEY" PRIMARY KEY (SEMESTERCODE));
 
-->Create the MODULELEADERS table which tracks the tutor which is a leader of a module 
CREATE TABLE "MODULELEADERS" (
 MODULELEADERID				NUMERIC(6) NOT NULL,
 TUTORID					NUMERIC(9) NOT NULL,
 OFFICEHOURS				CHAR(16),
 OFFICENO					CHAR(6),
 CONSTRAINT "MODULELEADERS_FOREIGN_KEY_TUTORID" FOREIGN KEY (TUTORID) REFERENCES "TUTORS" (TUTORID),
 CONSTRAINT "MODULELEADERS_PRIMARY_KEY" PRIMARY KEY (MODULELEADERID));
 
 -->Create the MODULES table which stores all the modules at the university
CREATE TABLE "MODULES" (
 MODULECODE					CHAR(6) NOT NULL,
 TITLE						CHAR(32),
 MODULELEADERID				NUMERIC(6),
 CONSTRAINT "MODULES_FOREIGN_KEY_MODULELEADER" FOREIGN KEY (MODULELEADERID) REFERENCES "MODULELEADERS" (MODULELEADERID),
 CONSTRAINT "MODULES_PRIMARY_KEY" PRIMARY KEY (MODULECODE));
 
-->Create the MODULETUTORS table which tracks the tutors on a module
CREATE TABLE "MODULETUTORS" (
 TUTORID					NUMERIC(9) NOT NULL,
 MODULECODE					CHAR(6) NOT NULL,
 CONSTRAINT "MODULETUTORS_FOREIGN_KEY_TUTORID" FOREIGN KEY (TUTORID) REFERENCES "TUTORS" (TUTORID),
 CONSTRAINT "MODULETUTORS_FOREIGN_KEY_MODULECODE" FOREIGN KEY (MODULECODE) REFERENCES "MODULES" (MODULECODE),
 CONSTRAINT "MODULETUTORS_PRIMARY_KEY" PRIMARY KEY (TUTORID, MODULECODE));
 
-->Create the MODULESCHEDULE table which tracks when modules take place
CREATE TABLE "MODULESCHEDULE" (
 MODSCHEDULEID				NUMERIC(8) NOT NULL,
 MODULECODE					CHAR(6) NOT NULL,
 SESSIONTYPE				CHAR(12) NOT NULL,
 STARTTIME					TIME,
 DAYOFWEEK					NUMERIC(1), -->Monday being 1, Sunday being 7
 DURATION					NUMERIC(3), -->Time in minutes
 LOCATION					CHAR(6),
 CONSTRAINT "MODULESCHEDULE_DURATION_ZERO" CHECK (DURATION > 0), -->Ensures that the duration is greater than 0, as an event cant happen for negative time
 CONSTRAINT "MODULECHEDULE_DAYOFWEEK_EXISTS" CHECK (DAYOFWEEK > 0 AND DAYOFWEEK < 8), -->Monday being 1, Sunday being 7. Dont let day 345 of the week exist for example
 CONSTRAINT "MODULESCHEDULE_FOREIGN_KEY_MODULECODE" FOREIGN KEY (MODULECODE) REFERENCES "MODULES" (MODULECODE),
 CONSTRAINT "MODULESCHEDULE_PRIMARY_KEY" PRIMARY KEY (MODSCHEDULEID));
 
-->Create the ASSESSMENTS table which details assessments for modules
CREATE TABLE "ASSESSMENTS" (
 ASSESSID					NUMERIC(4) NOT NULL,
 MODULECODE					CHAR(6) NOT NULL,
 DESCRIPTION				CHAR(128),
 DEADLINE					TIMESTAMP,
 CONSTRAINT "ASSESSMENTS_FOREIGN_KEY_MODULECODE" FOREIGN KEY (MODULECODE) REFERENCES "MODULES" (MODULECODE),
 CONSTRAINT "ASSESSMENTS_PRIMARY_KEY" PRIMARY KEY (ASSESSID));

-->Create the STUDENTSCHEDULE table which stores the students custom events
CREATE TABLE "STUDENTSCHEDULE" (
 STUDSCHEDULEID				NUMERIC(8) NOT NULL,
 STUDENTNO					NUMERIC(9) NOT NULL,
 STARTDATETIME				TIMESTAMP,
 DURATION					NUMERIC(3),
 DESCRIPTION				CHAR(128),
 CONSTRAINT "STUDENTSCHEDULE_DURATION_ZERO" CHECK (DURATION > 0), -->Ensures that the duration is greater than 0, as an event cant happen for negative time
 CONSTRAINT "STUDENTSCHEDULE_FOREIGN_KEY_STUDENTNO" FOREIGN KEY (STUDENTNO) REFERENCES "STUDENTS" (STUDENTNO),
 CONSTRAINT "STUDENTSCHEDULE_PRIMARY_KEY" PRIMARY KEY (STUDSCHEDULEID));
 
--Create the ENROLMENT table which stores the modules students are enrolled on
CREATE TABLE "ENROLMENT" (
 STUDENTNO					NUMERIC(9) NOT NULL,
 MODULECODE					CHAR(6) NOT NULL,
 SEMESTERCODE				CHAR(1) NOT NULL,
 ENROLYEAR					NUMERIC(4),
 CONSTRAINT "ENROLMENT_FOREIGN_KEY_STUDENTNO" FOREIGN KEY (STUDENTNO) REFERENCES "STUDENTS" (STUDENTNO),
 CONSTRAINT "ENROLMENT_FOREIGN_KEY_MODULECODE" FOREIGN KEY (MODULECODE) REFERENCES "MODULES" (MODULECODE),
 CONSTRAINT "ENROLMENT_FOREIGN_KEY_SEMESTERCODE" FOREIGN KEY (SEMESTERCODE) REFERENCES "SEMESTERS" (SEMESTERCODE),
 CONSTRAINT "ENROLMENT_PRIMARY_KEY" PRIMARY KEY (STUDENTNO, MODULECODE, SEMESTERCODE, ENROLYEAR));
 
-->Create the TUTORIALSESSIONS table which tracks the tutorials a student is going to
CREATE TABLE "TUTORIALSESSIONS" (
 STUDENTNO					NUMERIC(9) NOT NULL,
 MODSCHEDULEID				NUMERIC(8) NOT NULL,
 CONSTRAINT "TUTORIALSESSIONS_FOREIGN_KEY_STUDENTNO" FOREIGN KEY (STUDENTNO) REFERENCES "STUDENTS" (STUDENTNO),
 CONSTRAINT "TUTORIALSESSIONS_FOREIGN_KEY_MODSCHEDULEID" FOREIGN KEY (MODSCHEDULEID) REFERENCES "MODULESCHEDULE" (MODSCHEDULEID),
 CONSTRAINT "TUTORIALSESSIONS_PRIMARY_KEY" PRIMARY KEY (STUDENTNO, MODSCHEDULEID));
 

